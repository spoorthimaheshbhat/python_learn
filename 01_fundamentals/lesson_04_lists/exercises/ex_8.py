# Check whether:
#
# "apple" exists in a fruit list
# "grape" exists in a fruit list
#
# using in.

fruits = ["apple", "banana", "cherry", "pineapple"]

if "apple" in fruits:
    print(f"\"apple\" exists in fruit list")
else:
    print(f"\"apple\" not in fruit list")

if "grape" in fruits:
    print(f"\"grape\" exists in fruit list")
else:
    print(f"\"grape\" is not in fruit list")


